typedef struct {
    int V; // 顶点数
    int E; // 边数
    int F; // 面数
} poly;
void euler_formula(poly) {
    return poly.V - poly.E + poly.F = 2;
}
int main() {
    poly tetrahedron = {4, 6, 4}; //正四面体
    poly cube = {8, 12, 6}; //立方体或者正六面体
    poly octahedron = {6, 12, 8}; //正八面体 
    poly dodecahedron = {20, 30, 12}; //正十二面体
    poly icosahedron = {12, 30, 20}; //正二十面体
}
